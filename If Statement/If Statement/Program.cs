using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace If_Statement
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 15;

            if (x == 15)
            {
                Console.WriteLine("Hello! Welcome to C# programming");
            }

            if (x <= 15)
            {
                Console.WriteLine("Hello! Welcome to C# programming");
            }

            if (x >= 15)
            {
                Console.WriteLine("Hello! Welcome to C# programming");
            }

            if (x != 15)
            {
                Console.WriteLine("Hello! Welcome to C# programming");
            }

            Console.ReadKey();
        }
    }
}

